package sample;

public class Sample {

	public static void main(String[] args) {
		//mainメソッドが最初に実行される。
		//コンソールに出力するフォーマット
		System.out.println("Hello World");

		//変数宣言
		//データ型 変数名 = 値;
		int number = 10;
		number = 20;
		//String型は文字列
		String name = "正原";
		//doubleは実数（小数点）
		double number2 = 10.5;

		System.out.println(number);
		System.out.println(name);
		System.out.println(number2);
	}

}
