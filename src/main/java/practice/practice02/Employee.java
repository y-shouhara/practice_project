package practice.practice02;

public class Employee {
	//フィールド
	private String name;
	private int age;

	//メソッド
	public Employee() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
